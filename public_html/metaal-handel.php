// Verbeterde opbrengst calculator met voorrijkosten
function calculateEstimate() {
  const metalType = document.getElementById('metal-type').value;
  const weight = document.getElementById('weight').value;
  const resultContainer = document.getElementById('calculation-result');
  const resultText = document.getElementById('result-text');
  const pickup = document.getElementById('pickup').checked;
  
  // Valideer input
  if (!weight || weight <= 0) {
    resultText.textContent = 'Voer een geldig gewicht in';
    resultContainer.classList.add('active');
    return;
  }
  
  // Label aanpassen voor CV ketels (aantal vs gewicht)
  const quantityLabel = document.getElementById('quantity-label');
  if (metalType === 'cvketel') {
    quantityLabel.textContent = 'Aantal ketels (stuks)';
  } else {
    quantityLabel.textContent = 'Geschat Gewicht (kg)';
  }
  
  // Prijzen per metaal (prijzen in euro per kg)
  const prices = {
    'oudijzer': { min: 0.20, max: 0.30 },
    'koper': { min: 5.50, max: 7.00 },
    'aluminium': { min: 0.80, max: 1.80 },
    'rvs': { min: 1.20, max: 1.80 },
    'messing': { min: 3.80, max: 4.80 },
    'zink': { min: 1.20, max: 1.60 },
    'accu': { min: 0.50, max: 0.70 },
    'cvketel': { min: 20, max: 60 } // per stuk ipv per kg
  };
  
  // Bereken minimale en maximale opbrengst
  const minValue = Math.round(weight * prices[metalType].min * 100) / 100;
  const maxValue = Math.round(weight * prices[metalType].max * 100) / 100;
  
  // Bepaal de voorrijkosten en vergoeding op basis van de waarde
  let message = '';
  let totalMin = minValue;
  let totalMax = maxValue;
  
  if (pickup) {
    if (metalType === 'cvketel') {
      // Voor CV ketels andere logica 
      if (weight <= 1) {
        message = 'CV ketels worden GRATIS opgehaald en u ontvangt een vergoeding.';
      } else {
        message = 'Bij meerdere CV ketels ontvangt u een hogere vergoeding.';
      }
    } else {
      // Voor andere materialen
      if (maxValue < 100) {
        totalMin = 0;
        totalMax = 0;
        if (metalType === 'oudijzer' && weight < 300) {
          message = '<hr><b>Info:</b> Voor deze hoeveelheid brengen we €75,- voorrijkosten in rekening. We halen het materiaal wel GRATIS op maar zonder vergoeding.';
        } else {
          message = '<hr><b>Info:</b> Voor deze hoeveelheid brengen we €75,- voorrijkosten in rekening. Alternatief kunnen we het materiaal wel GRATIS ophalen maar zonder vergoeding.';
        }
      } else if (maxValue >= 150 && maxValue < 300) {
        message = '<hr><b>Goed nieuws:</b> Wij halen deze hoeveelheid GRATIS bij u op!';
      } else if (maxValue >= 300) {
        // 40% van de dagwaarde
        totalMin = minValue * 0.4;
        totalMax = maxValue * 0.4;
        message = '<hr><b>Uitstekend!</b> Bij deze hoeveelheid ontvangt u een vergoeding van 40% van de dagwaarde.';
      }
    }
  }
  
  // Update het resultaat
  let resultHTML = '';
  if (metalType === 'cvketel') {
    resultHTML = `Geschatte opbrengst: <strong>€${minValue.toFixed(2)} - €${maxValue.toFixed(2)}</strong> per CV ketel`;
  } else {
    resultHTML = `Marktwaarde: <strong>€${minValue.toFixed(2)} - €${maxValue.toFixed(2)}</strong>`;
    
    if (pickup && maxValue >= 300) {
      resultHTML += `<br>Uw vergoeding: <strong>€${totalMin.toFixed(2)} - €${totalMax.toFixed(2)}</strong>`;
    }
  }
  
  resultHTML += message;
  resultText.innerHTML = resultHTML;
  resultContainer.classList.add('active');
}

